# Sytax for for loop
# for variable in sequence:
#     # code to execute
# Example 1: Looping through a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)    
    
# Example 2: Looping through a range of numbers
for i in range(5):
    print(i)
    
# Example 3: Looping through a string
name = "Albin"
for char in name:
    print(char)
